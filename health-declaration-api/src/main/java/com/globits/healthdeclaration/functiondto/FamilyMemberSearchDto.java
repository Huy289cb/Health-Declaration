package com.globits.healthdeclaration.functiondto;

import java.util.UUID;

public class FamilyMemberSearchDto extends SearchDto {
	private UUID familyId;
	private UUID memberId;
	private Boolean hostFamily;
	private UUID practitionerId;

	public UUID getFamilyId() {
		return familyId;
	}

	public void setFamilyId(UUID familyId) {
		this.familyId = familyId;
	}

	public Boolean getHostFamily() {
		return hostFamily;
	}

	public void setHostFamily(Boolean hostFamily) {
		this.hostFamily = hostFamily;
	}

	public UUID getMemberId() {
		return memberId;
	}

	public void setMemberId(UUID memberId) {
		this.memberId = memberId;
	}

	public UUID getPractitionerId() {
		return practitionerId;
	}

	public void setPractitionerId(UUID practitionerId) {
		this.practitionerId = practitionerId;
	}

	public FamilyMemberSearchDto() {
		super();
	}

}
